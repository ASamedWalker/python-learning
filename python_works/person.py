# Person dictionary
person = {
  'first_name': 'Samed',
  'last_name': 'Walker',
  'age': 25,
  'city': 'New York',
}

first_name = person.get('first_name')
last_name = person.get('last_name')
age = person.get('age')
city = person.get('city')

print(first_name)
print(last_name)
print(age)
print(city)